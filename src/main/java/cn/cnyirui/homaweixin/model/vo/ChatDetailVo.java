package cn.cnyirui.homaweixin.model.vo;

import java.util.List;

import cn.cnyirui.homaweixin.model.po.ChatContent;

/**
 * 聊天记录信息
 * @author zhoujuhui
 *
 */
public class ChatDetailVo {

	/**
	 * 员工id
	 */
	private String senderId;
	/**
	 * 姓名
	 */
	private String senderName;
	/**
	 * 头像
	 */
	private String senderHeadImg;
	/**
	 * 服务器的当前时间戳
	 */
	private long currentTime;
	/**
	 * 会话的内容数组
	 */
	private List<ChatContentVo> contentVoList;
	
	
	public List<ChatContentVo> getContentVoList() {
		return contentVoList;
	}
	public void setContentVoList(List<ChatContentVo> contentVoList) {
		this.contentVoList = contentVoList;
	}
	public String getSenderName() {
		return senderName;
	}
	public void setSenderName(String senderName) {
		this.senderName = senderName;
	}
	public String getSenderHeadImg() {
		return senderHeadImg;
	}
	public void setSenderHeadImg(String senderHeadImg) {
		this.senderHeadImg = senderHeadImg;
	}
	public String getSenderId() {
		return senderId;
	}
	public void setSenderId(String senderId) {
		this.senderId = senderId;
	}
	public long getCurrentTime() {
		return currentTime;
	}
	public void setCurrentTime(long currentTime) {
		this.currentTime = currentTime;
	}
	
}
