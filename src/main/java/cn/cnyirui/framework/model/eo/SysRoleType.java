package cn.cnyirui.framework.model.eo;

/**
 * 默认角色
 * 
 * @author pengzhihua
 *
 */
public enum SysRoleType {

	SALER("SALER", "导购员"), DIRECTOR("DIRECTOR", "业务员"), MANAGER("MANAGER",
	        "中心经理"), HEADQUARTERS("HEADQUARTERS", "总部人员");

	private String value;
	private String text;

	private SysRoleType(String value, String text) {
		this.value = value;
		this.text = text;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}
}
